    private void btnProsesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnProsesActionPerformed
        try {
            koneksi();
            String namaPengguna = txtNama.getText();
            String genre = txtGenre.getText();
            int poinReward = Integer.parseInt(txtReward.getText());
            boolean aksesEksklusif = boxAkses.isSelected();

            String akses = aksesEksklusif ? "Ya" : "Tidak";

            // Insert ke database
            String sql = "INSERT INTO user_premium (nama_pengguna, genre_favorit, point, akses) VALUES ('"
                + namaPengguna + "','" + genre + "'," + poinReward + ",'" + akses + "')";
            Statement stat = conn.createStatement();
            stat.executeUpdate(sql);
            JOptionPane.showMessageDialog(this, "Data berhasil ditambahkan!");

            tampil(); // refresh tabel
            batal(); // reset form

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Poin reward harus berupa angka!", "Input Error", JOptionPane.ERROR_MESSAGE);
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Error SQL: " + e.getMessage());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Terjadi kesalahan: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnProsesActionPerformed

    private void btnProses1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnProses1ActionPerformed
        // TODO add your handling code here:
        // Update data
    try {
        koneksi();
        int baris = jTabel.getSelectedRow();
        if (baris == -1) {
            JOptionPane.showMessageDialog(this, "Pilih data yang ingin diupdate");
            return;
        }
        String namaPengguna = txtNama.getText();
        String genre = txtGenre.getText();
        int poinReward = Integer.parseInt(txtReward.getText());
        boolean aksesEksklusif = boxAkses.isSelected();
        String akses = aksesEksklusif ? "Ya" : "Tidak";

        // Ambil ID dari baris yang dipilih (asumsikan kolom pertama adalah ID, jika ada)
        // Jika tidak ada ID, bisa pakai nama_pengguna sebagai key (asumsi unik)
        String sql = "UPDATE user_premium SET genre_favorit='" + genre + "', point=" + poinReward + ", akses='" + akses + "' WHERE nama_pengguna='" + namaPengguna + "'";
        Statement stat = conn.createStatement();
        int update = stat.executeUpdate(sql);
        if (update > 0) {
            JOptionPane.showMessageDialog(this, "Data berhasil diupdate!");
            tampil();
            batal();
        } else {
            JOptionPane.showMessageDialog(this, "Data tidak ditemukan untuk diupdate!");
        }
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Poin reward harus berupa angka!", "Input Error", JOptionPane.ERROR_MESSAGE);
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Error SQL: " + e.getMessage());
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Terjadi kesalahan: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_btnProses1ActionPerformed

