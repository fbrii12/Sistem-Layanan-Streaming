    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        // TODO add your handling code here:
        try {
        koneksi();
        int baris = jTabel.getSelectedRow();
        if (baris == -1) {
            JOptionPane.showMessageDialog(this, "Pilih data yang ingin dihapus");
            return;
        }
        String namaPengguna = jTabel.getValueAt(baris, 0).toString();

        String sql = "DELETE FROM user_premium WHERE nama_pengguna='" + namaPengguna + "'";
        Statement stat = conn.createStatement();
        int delete = stat.executeUpdate(sql);
        if (delete > 0) {
            JOptionPane.showMessageDialog(this, "Data berhasil dihapus!");
            tampil();
            batal();
        } else {
            JOptionPane.showMessageDialog(this, "Data tidak ditemukan untuk dihapus!");
        }
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Error SQL: " + e.getMessage());
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Terjadi kesalahan: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_btnHapusActionPerformed

