        public void tampil() {
        DefaultTableModel tabelhead = new DefaultTableModel();
        tabelhead.addColumn("Nama Pengguna");
        tabelhead.addColumn("Genre Favorit");
        tabelhead.addColumn("Point");
        tabelhead.addColumn("Akses");
        try {
            koneksi();
            String sql = "SELECT * FROM user_premium";
            Statement stat = conn.createStatement();
            ResultSet res = stat.executeQuery(sql);
            while (res.next()) {
                tabelhead.addRow(new Object[]{
                    res.getString(2), // Nama Konser
                    res.getString(3), // Nama Konser
                    res.getString(4), // Jumlah Konser
                    res.getString(5), // Jumlah Konser
                });
            }
            jTabel.setModel(tabelhead);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error: " + e.getMessage());
        }
    }   
